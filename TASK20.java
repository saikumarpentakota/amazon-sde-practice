public class TASK20 {
    public static void main(String[] args){
        char[] Name = {'s','a','i','k','u','m','a','r'};
        int n = Name.length;
        System.out.println("There are " + n + " letters in my name.");
        System.out.println("My name is: ");
        for (int i = 0 ; i < n ; i++){
            System.out.print(Name[i]);
        }
        System.out.println();
    }
}
